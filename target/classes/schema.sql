-- ── Sequences ──
CREATE SEQUENCE USERS_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE JOBS_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE APPLICATIONS_SEQ START WITH 1 INCREMENT BY 1;

-- ── Users Table ──
CREATE TABLE USERS (
    USER_ID         NUMBER(19)      DEFAULT USERS_SEQ.NEXTVAL PRIMARY KEY,
    EMAIL           VARCHAR2(200)   NOT NULL UNIQUE,
    PASSWORD_HASH   VARCHAR2(255)   NOT NULL,
    ROLE            VARCHAR2(30)    NOT NULL,
    IS_ACTIVE       NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT      TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- ── Student Profile Table ──
CREATE TABLE STUDENT_PROFILE (
    USER_ID         NUMBER(19)      PRIMARY KEY,
    FULL_NAME       VARCHAR2(200),
    PHONE           VARCHAR2(20),
    MAJOR           VARCHAR2(100),
    GRADUATION_YEAR NUMBER(4),
    LINKEDIN_URL    VARCHAR2(500),
    RESUME_URL      VARCHAR2(500),
    CONSTRAINT FK_STUDENT_USER FOREIGN KEY (USER_ID)
        REFERENCES USERS (USER_ID)
);

-- ── Employer Profile Table ──
CREATE TABLE EMPLOYER_PROFILE (
    USER_ID         NUMBER(19)      PRIMARY KEY,
    COMPANY_NAME    VARCHAR2(200),
    INDUSTRY        VARCHAR2(100),
    WEBSITE         VARCHAR2(500),
    DESCRIPTION     VARCHAR2(2000),
    CONSTRAINT FK_EMPLOYER_USER FOREIGN KEY (USER_ID)
        REFERENCES USERS (USER_ID)
);

-- ── Jobs Table ──
CREATE TABLE JOBS (
    JOB_ID              NUMBER(19)      DEFAULT JOBS_SEQ.NEXTVAL PRIMARY KEY,
    EMPLOYER_USER_ID    NUMBER(19)      NOT NULL,
    TITLE               VARCHAR2(200)   NOT NULL,
    LOCATION            VARCHAR2(200),
    JOB_TYPE            VARCHAR2(50),
    DESCRIPTION         VARCHAR2(4000),
    DEADLINE            TIMESTAMP,
    IS_ACTIVE           NUMBER(1)       DEFAULT 1 NOT NULL,
    CREATED_AT          TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_JOB_EMPLOYER FOREIGN KEY (EMPLOYER_USER_ID)
        REFERENCES USERS (USER_ID)
);

-- ── Applications Table ──
CREATE TABLE APPLICATIONS (
    APPLICATION_ID      NUMBER(19)      DEFAULT APPLICATIONS_SEQ.NEXTVAL PRIMARY KEY,
    STUDENT_USER_ID     NUMBER(19)      NOT NULL,
    JOB_ID              NUMBER(19)      NOT NULL,
    STATUS              VARCHAR2(30)    NOT NULL,
    APPLIED_AT          TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT          TIMESTAMP,
    RESUME_URL_AT_APPLY VARCHAR2(500),
    EMPLOYER_NOTES      VARCHAR2(2000),
    CONSTRAINT FK_APP_STUDENT FOREIGN KEY (STUDENT_USER_ID)
        REFERENCES USERS (USER_ID),
    CONSTRAINT FK_APP_JOB FOREIGN KEY (JOB_ID)
        REFERENCES JOBS (JOB_ID),
    CONSTRAINT UK_APP_STUDENT_JOB UNIQUE (STUDENT_USER_ID, JOB_ID)
);

-- ── Indexes for Performance ──
CREATE INDEX IDX_JOBS_EMPLOYER ON JOBS (EMPLOYER_USER_ID);
CREATE INDEX IDX_JOBS_ACTIVE ON JOBS (IS_ACTIVE);
CREATE INDEX IDX_APP_STUDENT ON APPLICATIONS (STUDENT_USER_ID);
CREATE INDEX IDX_APP_JOB ON APPLICATIONS (JOB_ID);
CREATE INDEX IDX_APP_STATUS ON APPLICATIONS (STATUS);
